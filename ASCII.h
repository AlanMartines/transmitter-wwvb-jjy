//                GND 4|    |5 PB0
//                     +----+
//
//Arduino Nano
//       +----+=====+----+
//       |    | USB |    |
// SCK 13| D13+-----+D12 |12 MISO
//       |3V3        D11~|11 MOSI
//       |Vref       D10~|10 SS
//     14| A0/D14     D9~|9 => WWVB ANTENNA
//     15| A1/D15     D8 |8
//     16| A2/D16     D7 |7
//     17| A3/D17     D6~|6
// SDA 18| A4/D18     D5~|5
// SCL 19| A5/D19     D4 |4
//     20| A6         D3~|3 INT1
//     21| A7         D2 |2 INT0
//       | 5V        GND |
//       | RST       RST |
//       | GND       TX1 |0
//       | Vin       RX1 |1
//       |  5V MOSI GND  |
//       |   [ ][ ][ ]   |
//       |   [*][ ][ ]   |
//       | MISO SCK RST  |
//       +---------------+
//
//Arduino Micro
//       +--------+=====+---------+
//       |        | USB |         |
// SCK 13| D13    +-----+     D12 |12 MISO
//       |3V3                 D11~|11 MOSI
//       |Vref                D10~|10 SS
//     14| A0/D14              D9~|9 => WWVB ANTENNA
//     15| A1/D15              D8 |8
//     16| A2/D16              D7 |7
//     17| A3/D17              D6~|6
// SDA 18| A4/D18              D5~|5
// SCL 19| A5/D19              D4 |4
//     20| A6                  D3~|3 INT1
//     21| A7                  D2 |2 INT0
//       | 5V                 GND |
//       | RST                RST |
//       | GND                TX1 |0
//       | Vin                RX1 |1
//       |MISO MISO[*][ ]VCC    SS|
//       |SCK   SCK[ ][ ]MOSI MOSI|
//       |      RST[ ][ ]GND      |
//       +------------------------+
//
//Sparkfun Pro Micro
//                  +----+=====+----+
//                  |[J1]| USB |    |
//                 1| TXO+-----+RAW |
//                 0| RXI       GND |
//                  | GND       RST |
//                  | GND       VCC |
//             SDA 2| D2         A3 |21
//             SCL 3|~D3         A2 |20
//                 4| D4         A1 |19
//                 5|~D5         A0 |18
//                 6|~D6        D15 |15 SCK
//                 7| D7        D14 |14 MISO
//                 8| D8        D16 |16 MOSI
//WWVB ANTENNA  <= 9|~D9        D10~|10
//                  +---------------+
//
//                 +--------+
//                 | millis |
//+------------+---+--------+--------+--------+--------+--------+
//| Chip       |   | Timer0 | Timer1 | Timer2 | Timer3 | Timer4 |
//+------------+---+--------+--------+--------+--------+--------+
//| ATtiny85   | A |   D0   |   D1   |   --   |   --   |   --   |
//|            | B |   D1   | C=D3   |   --   |   --   |   --   |
//|       M=D4 (Toggled in software) |   --   |   --   |   --   |
//+------------+---+--------+--------+--------+--------+--------+
//| ATmega328p | A |   D6   | M=D9   |  D12   |   --   |   --   |
//|            | B |   D5   |  D10   | C=D3   |   --   |   --   |
//+------------+---+--------+--------+--------+--------+--------+
//| ATmega32u4 | A |  D11   | C=D9   |   --   | M=D5   |  D13   |
//+------------+---+--------+--------+--------+--------+--------+
